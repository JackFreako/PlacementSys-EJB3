    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (type == 0) {
            //save new rule
            try {
                rule = this.getRule();
                if (rule != null && ruleBL.save(rule)) {
                    parent.prepareDataVector();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            //update existing rule
            try {          
                rule = this.getRule();
                if (rule != null && ruleBL.update(rule)) {
                    parent.prepareDataVector();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        this.dispose();
    }//GEN-LAST:event_saveBtnActionPerformed

