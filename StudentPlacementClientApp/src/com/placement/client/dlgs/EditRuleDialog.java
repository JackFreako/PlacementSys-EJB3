    private Rule getRule() {
        try {
            String top = topTxt.getText();
            String female = femaleTxt.getText();
            String region = regionTxt.getText();
            String diss = disabilityTxt.getText();
            String cutpoint = cutPointTxt.getText();
            Rule r = new Rule();
            if (type != 0) {
                r.setId(this.rule.getId());
            }
            r.setTopPercentage(Double.parseDouble(top));
            r.setFemalePercentage(Double.parseDouble(female));
            r.setRegionPercentage(Double.parseDouble(region));
            r.setDisabilityPercentage(Double.parseDouble(diss));
            r.setCutPoint(Double.parseDouble(cutpoint));
            return r;
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }
